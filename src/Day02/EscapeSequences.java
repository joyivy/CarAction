package Day02;

public class EscapeSequences {

    public static void main(String[] args) {

        System.out.println("Hello Students\nToday we have Java Class"); // \n sequence adds a new line

        System.out.println("----------------------------------------------");

        System.out.println("\tJava Programming Language");
        System.out.println("\t\t\tJava Developer Course Batch 1");

        System.out.println("----------------------------------------------");

        System.out.println("\\");

        System.out.println("----------------------------------------------");

        System.out.println("My favorite TV show is \"Orange is the new black\"");
    }
}
