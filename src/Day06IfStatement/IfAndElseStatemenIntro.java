package Day06IfStatement;

public class IfAndElseStatemenIntro {
    public static void main(String[] args) {
        int age = 21;
        if (age >= 21) {
            System.out.println("eligible");
        }
        if (age < 21) {
            System.out.println(" nor eligible");
        }
        System.out.println("+++++++++++++++++++++++++");

        if (age >= 21) {
            System.out.println("Eligible");
        } else {   //otherwise
            System.out.println(" Not Elegible");

        }
    }
}
