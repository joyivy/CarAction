package Day06IfStatement;

public class PostNegZero {
    public static void main(String[] args) {

        int number = 20;
        if (number < 0){
            System.out.println(number + "Negative");}
        if (number > 0){
            System.out.println(number + " Positive");
        }
        if( number == 0){
            System.out.println( number + " Zero ");
        }
    }



}

